package algorithms.search;

abstract class AState {
    int x;
    int y;

    public AState(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    abstract public boolean isGoalState(ISearchable game);

}
